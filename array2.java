public class array2 {

     public static void main(String args[])
     {
         int[][] marks ={{4,7,8}, {5,8,7}, {7,9,14}};

         for (int i=0;i<3;i++)
         {
            for (int j=0;j<3;j++)
            {
         System.out.print(marks[i][j] + " ");
         }
         System.out.println();
     }
}
}
